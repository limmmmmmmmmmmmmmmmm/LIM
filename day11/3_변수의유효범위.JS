//전역변수와 지역변수의 활용 예
let 배열 = [];

function 등록() {
    //let 배열 = []; // 0.배열선언, 함수가 끝나면 해당{} 안에 있는 메모리 사라짐

    //let 입력 = prompt(`성명`); //1. 입력받고 변수에 저장한다.  //사라짐
    let 입력 = document.querySelector(`#성명`).value;

    배열.push(입력); //2. 입력받은 데이터를 배열에 저장한다.
    document.querySelector(`#출력박스`).innerHTML = 배열; //3. html에 출력

    document.querySelector(`#성명`).value = ''; //검색하면 사라짐



}

