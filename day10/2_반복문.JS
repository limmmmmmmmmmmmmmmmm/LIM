//1. 사용자가 입력한 번호 목록/배열
let 입력번호목록 = []
//2. 자동 생성된 번로 목록/배열
let 추첨번호목록 = []

//3. 6개의 수 를 입력받아 입력번호목록에 저장
    //조건 : 1~45 사이의 숫자인 기능,중복 제외
for(let i = 1; i<=6; i++){
    let 입력번호 = Number(prompt(`${i}번째 숫자 입력`)) //입력받고 변수에 저장한다.
    // Number(데이터) : 숫자타입 변환 함수, 만일 문자이면 NaN
    // NaN(nat a Number)  : NaN은 비교연산이 불가능하므로 
        //isNaN(데이터변수) : 숫자가 아니면 true 숫자이면 false
    //=================조건1======================//
        //만약에 입렵된번호가 1보다 작고 45보다 크거나 숫자가 아니면    
    입력번호목록.push(입력번호);

}

//4. 6개의 수를 난수/임의 생성해서 추정번호목록에 저장
    //조건 : 1 ~ 45 사이의숫자만 가능 중복제외


//5. 두 배열의 동일한 숫자가 몇개인지 체크
let 일치수 = 0;
    // - 0번 인덱스부터 마지막 인덱스까지
for(let i = 0; i<= 입력번호목록.length-1; i++){
    // i번째 입력번호목록의 값이 추첨번호목록에 존재하면
    if(추첨번호목록.indexOf(입력번호목록[i])! = -1){일치수++;}// 일치수 1증가

}


//6. HTML 출력
document.querySelector(`#입력번호목록`).innerHTML = 입력번호목록
document.querySelector(`#추첨번호목록`).innerHTML = 추첨번호목록